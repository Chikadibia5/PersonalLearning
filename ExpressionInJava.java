package saturdayproject;

public class ExpressionInJava {

	public static void main(String[] args) {
		
		// expression = operands & operators
		//operand = values, variables, numbers, quantity
		//operators = + - * / % 
		
		
		int friends = 10;
		int family = 12;
		int classmate = 15;
		double colleagues = 37;
		
		friends = friends - 7;
		family = family * 2;
		classmate = classmate - 9 + 4;
		colleagues =colleagues  / 4;
		
		System.out.println(friends);
		System.out.println(family);
		System.out.println(classmate);
		System.out.println(colleagues);

	}

}
